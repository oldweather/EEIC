sub short_from_long {
    my $Long = shift;
    if(exists($ShortNames{$Long})) { return $ShortNames{$Long}; }
    else { return; }
}

sub long_from_short {  # Slow, but does it matter?
    my $Short = shift;
    foreach my $Long ( keys(%ShortNames)) {
        if($ShortNames{$Long} eq $Short) { return $Long; }
    }
    return;
}

sub formatted_from_long {
    my $Long = shift;
    if(exists($FormattedNames{$Long})) { return $FormattedNames{$Long}; }
    else { return; }
}

